public class FitnessTracker {
    String name;
    int datOfBirth;
    int monthOfBirth;
    int yearsOfBirth;
    String mail;
    int telNumber;
    String surname;
    int weight;
    int pressure;
    int numberOfSteps;

    public FitnessTracker(String name, int datOfBirth, int monthOfBirth, int yearsOfBirth,
                          String mail, int telNumber,
                          String surname, int weight, int pressure, int numberOfSteps) {
        this.name = name;
        this.datOfBirth = datOfBirth;
        this.monthOfBirth = monthOfBirth;
        this.yearsOfBirth = yearsOfBirth;
        this.mail = mail;
        this.telNumber = telNumber;
        this.surname = surname;
        this.weight = weight;
        this.pressure = pressure;
        this.numberOfSteps = numberOfSteps;
    }




}
